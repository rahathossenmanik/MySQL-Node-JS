var mysql      = require('mysql');
var sql = "CREATE TABLE Demo (name VARCHAR(255), Address VARCHAR(255))";
var connection = mysql.createConnection({
	database : 'NodeDB',
    host     : 'localhost',
	port	 : '3306',
    user     : 'root',
    password : ''
});
connection.query(sql, function (err, result) {
    if (err) throw err;
    console.log("Table Created!");
});